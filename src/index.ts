// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
//  1. Define your toolâ€™s Zod â€œraw shapeâ€ & types
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const VIBE_CHECK_TOOL = 'vibe-check';

// This object is a ZodRawShape (stringâ†’ZodType) that registerTool accepts
const VibeCheckParams: ZodRawShape = {
  plan:           z.string(),
  userRequest:    z.string(),
  thinkingLog:    z.string().optional(),
  availableTools: z.array(z.string()).optional(),
  splitOutput:    z.boolean().optional(),
  confidence:     z.number().optional(),
};

